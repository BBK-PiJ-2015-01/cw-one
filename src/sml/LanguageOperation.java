package sml;

/**
 * Instances of valid language operations. Typically these would be upper
 * case but as the supplied instructions are all lower case then these are
 * implemented here to ensure uniqueness.
 * 
 * @author sbaird02
 *
 */
public enum LanguageOperation {

	lin, out, add, sub, mul, div, bnz, brn;

}
